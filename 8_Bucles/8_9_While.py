#➡️ Un while es una estructura de control en Python que permite ejecutar un bloque de código repetidamente mientras una condición sea verdadera.
#➡️ La sintaxis básica de un bucle while es:
#➡️ while condición:
#➡️     # bloque de código a ejecutar mientras la condición sea verdadera
#➡️ Ejemplo de uso de un bucle while
contador = 0
#➡️ Inicializa una variable contador en 0
while contador < 5:
    #➡️ Mientras el contador sea menor que 5
    print("Contador:", contador)
    #➡️ Imprime el valor actual del contador
    contador += 1
    #➡️ Incrementa el contador en 1